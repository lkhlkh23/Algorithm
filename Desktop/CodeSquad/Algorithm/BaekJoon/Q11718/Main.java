package Q11718;

import java.io.*;

public class Main {
	/*
	 	문제 : 그대로 출력하기
	 	url : https://www.acmicpc.net/problem/11718
	 	재풀이 : O
	*/

	public static void main(String[] args) throws IOException {
		BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
		int a = 0;

		while((a = br.read())!= -1) {
			System.out.print((char)a);
		}
	}

}